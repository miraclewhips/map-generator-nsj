<template>
  <Button @click="exportToJson()" :class="buttonClass" text="JSON" title="Export as JSON" />
</template>

<script setup>
import Button from './Elements/Button.vue';

const props = defineProps({
  selection: Array,
	filename: { type: String, default: 'Generated map' },
	buttonClass: { type: String, default: 'bordered-success' }
});

const exportToJson = () => {
  let data = [];
  props.selection.forEach((country) => (data = data.concat(country.found)));
  const dataUri = 'data:application/json;charset=utf-8,' + encodeURIComponent(JSON.stringify({ customCoordinates: data }));
  const fileName = `${props.filename} (${data.length} location${data.length > 1 ? 's' : ''}).json`;
  const linkElement = document.createElement('a');
  linkElement.href = dataUri;
  linkElement.download = fileName;
  linkElement.click();
};
</script>
